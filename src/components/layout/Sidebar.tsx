'use client'

import Link from 'next/link'
import { usePathname } from 'next/navigation'
import { useState, useEffect } from 'react'
import { cn } from '@/lib/utils'

const navigation = [
  { name: 'Overview', href: '/', icon: HomeIcon },
  { name: 'WhatsApp', href: '/whatsapp', icon: ChatIcon },
  { name: 'Follow-ups', href: '/followups', icon: BellIcon },
  { name: 'Listings', href: '/listings', icon: BuildingIcon },
  { name: 'Bookings', href: '/bookings', icon: CalendarIcon },
  { name: 'Conversations', href: '/conversations', icon: MessageIcon },
  { name: 'Lead Hub', href: '/leads', icon: LeadsIcon },
  { name: 'Settings', href: '/settings', icon: SettingsIcon },
]

export default function Sidebar() {
  const pathname = usePathname()
  const [open, setOpen] = useState(false)

  // Close the mobile drawer whenever the route changes
  useEffect(() => { setOpen(false) }, [pathname])

  // Lock body scroll while the mobile drawer is open
  useEffect(() => {
    if (!open) return
    const prev = document.body.style.overflow
    document.body.style.overflow = 'hidden'
    return () => { document.body.style.overflow = prev }
  }, [open])

  return (
    <>
      {/* Mobile top bar (hidden on desktop) */}
      <div className="lg:hidden fixed top-0 inset-x-0 z-40 h-14 flex items-center gap-3 px-4 bg-[#050505] border-b border-white/5">
        <button
          aria-label="Open menu"
          onClick={() => setOpen(true)}
          className="-ml-1 p-2 rounded-lg text-zinc-300 hover:text-white hover:bg-white/5 transition-colors"
        >
          <MenuIcon className="w-6 h-6" />
        </button>
        <h1 className="text-lg font-bold tracking-tight">JCKSN</h1>
      </div>

      {/* Drawer overlay (mobile only) */}
      {open && (
        <div
          className="lg:hidden fixed inset-0 z-40 bg-black/60 backdrop-blur-sm"
          onClick={() => setOpen(false)}
          aria-hidden
        />
      )}

      {/* Sidebar panel — off-canvas drawer on mobile, static on desktop */}
      <div
        className={cn(
          'fixed left-0 top-0 h-full w-64 bg-[#050505] border-r border-white/5 flex flex-col z-50 transition-transform duration-300 ease-out',
          'lg:translate-x-0',
          open ? 'translate-x-0' : '-translate-x-full'
        )}
      >
        <div className="p-6 border-b border-white/5 flex items-start justify-between">
          <div>
            <h1 className="text-xl font-bold tracking-tight">JCKSN</h1>
            <p className="text-xs text-zinc-500 mt-1">WhatsApp Assistant</p>
          </div>
          <button
            aria-label="Close menu"
            onClick={() => setOpen(false)}
            className="lg:hidden -mr-1 p-1 rounded-lg text-zinc-400 hover:text-white hover:bg-white/5 transition-colors"
          >
            <CloseIcon className="w-6 h-6" />
          </button>
        </div>

        <nav className="flex-1 p-4 space-y-1 overflow-y-auto">
          {navigation.map((item) => {
            const Icon = item.icon
            const isActive = pathname === item.href
            return (
              <Link
                key={item.name}
                href={item.href}
                onClick={() => setOpen(false)}
                className={cn(
                  'flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm font-medium transition-all',
                  isActive
                    ? 'bg-white text-black'
                    : 'text-zinc-400 hover:bg-white/5 hover:text-white'
                )}
              >
                <Icon className={cn('w-5 h-5', isActive && 'text-black')} />
                {item.name}
              </Link>
            )
          })}
        </nav>

        <div className="p-4 border-t border-white/5">
          <div className="flex items-center gap-3 px-3 py-2">
            <div className="w-8 h-8 rounded-full bg-white/10 flex items-center justify-center">
              <span className="text-xs font-bold">PA</span>
            </div>
            <div>
              <p className="text-sm font-medium">Property Agent</p>
              <p className="text-xs text-zinc-500">Admin</p>
            </div>
          </div>
        </div>
      </div>
    </>
  )
}

